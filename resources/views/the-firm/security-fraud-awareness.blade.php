          <h6>As our reliance on the internet and digital devices for business and personal use 
              increases, so do opportunities for criminals seeking to steal information for 
              financial gain. Cyber criminals and fraudsters are also becoming more savvy in their 
              attempts to lure people into clicking suspicious links, downloading email attachments, or “connecting” 
              on social media, which are often gateways to stealing sensitive information. Fraudsters may pose as 
              legitimate organizations and create fraudulent websites, send emails, or make phone calls 
              to solicit monetary payments. These scams are complex as the perpetrators often use 
              genuine real employee names and replicate proprietary documentation.
              <br><br>
              Yielda places great importance on cybersecurity and fraud prevention and has programs and technical controls in place to protect client accounts and information. To help improve your personal cybersecurity posture, we offer the following information about cyber threats and guidance to help protect you, your family, and your employer from falling victim to a cyber-attack or fraud scam.
          </h6>
            <hr>
            <h6><b>Understanding Cybersecurity Threats</b>
            <br>
            Any organization or individual can be a target of cyber criminals. Here are some of the most common tactics and types of attacks employed by these actors:
            <ul>
                <li>
                    <b>Malicious Emails and Websites</b><br>
                    An unsuspecting e-mail from your bank or favorite retailer may secretly be an attempt to steal your identity or personal information. “Phishing” is common tactic of cyber criminals that relies on “spoofed” e-mails or fraudulent websites (that look and feel like a well-known website) to collect personal and financial information or infect your machine with malware and viruses. Criminals use this stolen information to commit identity theft, credit card fraud and other crimes. Phishing can also occur by telephone and is becoming increasingly prevalent on social media and professional networking sites.
                    <br><br>
                    When you click a malicious link, you may unknowingly install malware on your device. Malware refers to software that is intentionally designed to cause damage to a digital device. The most common form of malware is a virus, which is typically designed to give the criminals who create it some sort of access to the infected devices.  Ransomware is another type of malware that is becoming increasingly prevalent. Ransomware accesses a victim’s files, locks and encrypts them and then demands the victim to pay a ransom to get them back. Ransomware is like the “digital kidnapping” of valuable data – from personal photos and memories to client information, financial records and intellectual property. Any individual or organization could be a potential ransomware target.
                </li>
                <li>
                    <b>Credential-based Attacks</b><br>
                    If you use the same username and password combination across different websites or services, you are particularly susceptible to this cybercrime technique where stolen account credentials are used to gain unauthorized access to a user’s various other online accounts. Credential stuffing attacks can often go unnoticed until funds are transferred. 
                </li>
                <li>
                    <b>Social Media Impersonation</b><br>
                    Criminals are increasingly using social media to build relationships with victims and ultimately steal data. Typically, these actors create fake accounts that appear (and claim) to be official accounts for an individual or organization. Social media impersonation can also refer to the takeover of real accounts. These accounts can be used for phishing activities or causing an individual or a company reputational damage.
                </li>
            </ul>
            <h6>
            <hr>

            <h6><b>How You Can Protect Yourself</b> 
            <ul>
                <li><b>Establish Secure Email Protocols: </b>Emails continue to be a common entry point for hackers for performing online fraud. Do not click on links or open attachments from suspicious-looking emails. Expand your communication protocol to verify sensitive information, such as wire instructions, in person or by telephone. Generally, Goldman Sachs will never send wiring instructions via email.</li>
                <li><b>Employ Password Management: </b>Use lengthy, unique, and complex passwords — a great first step toward stopping bad actors. In fact, cybersecurity best practices suggest utilizing long, memorable, and hard-to guess passwords such as a favorite song lyric. Avoid reusing passwords. Consider using a password application, such as LastPass, Password or Dashlane to help manage multiple complex passwords.</li>
                <li><b>Enable 2-Step Authentication Measures: </b>Where available, use 2-factor authentication for account login (2FA) a.k.a. two-step verification or multi-factor authentication, commonly done via a PIN sent over text message or email and done most securely when a hardware token or phone application is used. At a minimum, enable this capability for your email, cellular provider, financial websites, password manager, cloud file storage and social media.</li>
                <li><b>Reduce Your Public Online Footprint: </b>Periodically review all your online accounts. Reduce and/ or obfuscate personal information on the internet, remove unnecessary data, delete unused accounts, and avoid sharing or reusing passwords across accounts to minimize exposure.</li>
            </ul>
            </h6>
            <hr>
            <h6>
                <b>Understanding Financial Fraud</b><br>
                Financial fraud occurs when someone takes money or other assets from you through deception or criminal activity. Here are some common examples of financial fraud:
                <ul>
                    <li>
                        <b>Investment Scams</b><br>
                        Investment scams involve getting you or your business to agree to a financial transaction on the promise of a questionable financial opportunity. To perpetrate these scams, fraudsters typically present the opportunity make contact by email, through a website, or by phone. These offers are typically low risk –high reward investments that typically sound “too good to be true”— because they are! To evaluate whether you are the target of an investment scam, you should consider:
                        <ul>
                            <li>How were you contacted? Any contact with Yielda will come from an @yielda.com e-mail address (not from a free email account such as Yahoo, Gmail or any other domain outside of “@yielda.com”) and/or be found on this website</li>
                            <li>Did I find the investment opportunity through a website not associated with Yielda? E.g. a comparison website</li>
                            <li>Have I provided my personal information on a website not associated with Yielda?</li>
                            <li>Have I been contacted by cold call or e-mail offering a low risk – high return investment opportunity?</li>
                            <li>Does the e-mail or documentation contain numerous spelling errors or misprints?</li>
                            <li>Have I provided photo ID or proof of address documentation? If you have, consider notifying the organisation that issued them and contacting your regional fraud prevention service</li>
                            <li>Was I pressured into making a money transfer to avoid missing an opportunity?</li>
                        </ul>
                    </li>
                    <li>
                        <b>Identity Theft</b><br>
                        Identity theft occurs when someone steals your personal information and uses it without your permission. Examples of how your information could be used include opening bank accounts, taking out credit cards and loans or applying for government benefits and documents in your name.
                        There is no definite rule on how to protect yourself from identity theft however, in addition to the cybersecurity good practices listed above, you can protect yourself by:
                        <ul>
                            <li>Not sharing your personal data with anyone/any site you’re not familiar with</li>
                            <li>Safely disposing of unwanted documents such as utility bills or bank statements</li>
                        </ul>
                    </li>
                    <li>
                        <b>Business Email Compromise</b><br>
                        Business Email Compromise (BEC) scams are carried out when a cybercriminal compromises 
                        legitimate business or personal email accounts to intercept the communication between the 
                        victim and their business partner or to conduct unauthorized transfers of funds. 
                        Fraudsters commonly tend to intercept email wire instructions from investments firms, 
                        real estate agencies, and art dealers to then impersonate a trusted source.
                        In addition to the cybersecurity good practices listed above, protect yourself by:
                        <ul>
                            <li>Confirming the payment instructions with the recipient verbally, not by email</li>
                            <li>Watching for irregularities when receiving transfer instructions or sudden change of payment information via email</li>
                        </ul>
                    </li>
                </ul>
            </h6>
